/**
 * gui_mouse_draw_buffer - 往缓冲区里面绘制鼠标
 */
static void gui_mouse_draw_buffer(GUI_COLOR *buffer, char *data)
{
    int x, y;
	for (y = 0; y < gui_mouse.height; y++) {
		for (x = 0; x < gui_mouse.width; x++) {
			if (data[y * gui_mouse.width + x] == 0) {
                /* 透明色 */
                buffer[y * gui_mouse.width + x] = COLOR_ARGB(0, 0, 0, 0);
            } else if (data[y * gui_mouse.width + x] == 1) {
				/* 边框色 */
                buffer[y * gui_mouse.width + x] = COLOR_WHITE;
            } else if (data[y * gui_mouse.width + x] == 2) {
                /* 填充色 */
                buffer[y * gui_mouse.width + x] = COLOR_BLACK;
            }
		}
	}
}

